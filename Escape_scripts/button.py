import pygame
import sys

class ButtonBordered():
	def __init__(self, image, pos, text_input, font, base_color, hovering_color):
		self.image = image
		self.x_pos = pos[0]
		self.y_pos = pos[1]
		self.font = font
		self.base_color, self.hovering_color = base_color, hovering_color
		self.text_input = text_input
		self.text = self.font.render(self.text_input, True, self.base_color)

		# If no image is provided, use the rendered text itself as the button
		if self.image is None:
			self.image = self.text

		# Rectangles for positioning
		self.rect = self.image.get_rect(center=(self.x_pos, self.y_pos))
		self.text_rect = self.text.get_rect(center=(self.x_pos, self.y_pos))

		# Border thickness and padding around the text
		self.border_padding = 5  # Adjust this for thickness
		self.border_rect = pygame.Rect(
			self.text_rect.left - self.border_padding,
			self.text_rect.top - self.border_padding,
			self.text_rect.width + 2 * self.border_padding,
			self.text_rect.height + 2 * self.border_padding,
		)

	def update(self, screen):
		# Draw the black border closely around the text
		pygame.draw.rect(screen, "Black", self.border_rect, border_radius=5)  # border_radius adds rounded corners

		# Draw the button text or image
		if self.image is not None:
			screen.blit(self.image, self.rect)
		screen.blit(self.text, self.text_rect)

	def checkForInput(self, position):
		# Check if the mouse position is within the text area
		return self.text_rect.collidepoint(position)

	def changeColor(self, position):
		# Change text color based on hover state
		if self.text_rect.collidepoint(position):
			self.text = self.font.render(self.text_input, True, self.hovering_color)
		else:
			self.text = self.font.render(self.text_input, True, self.base_color)

